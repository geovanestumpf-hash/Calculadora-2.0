subtração = num_1 - num_2 # Cria a variável subtração
multiplicação = num_1 * num_2 # Cria a variável multiplicação
divisão = num_1 / num_2 # Cria a variável divisão
divisão_inteira = num_1 // num_2 # Cria a variável divisão inteira
potenciação = num_1 ** num_2 # Cria a variável potenciação
print("A soma dos números é:", soma) # Soma os valores
print("A subtração dos números é:", subtração) # Subtrai os valores
print("A multiplicação dos números é:", multiplicação) # Multiplica os valores
print("A divisão dos números é:", divisão) # Divide os valores
print("A divisão inteira dos números é:", divisão_inteira) # Informa a divisão inteira
print("A potenciação dos números é:", potenciação) # Informa a potenciação

