"""## Inserção dos Números"""

print("Vamos ao cálculo, lembrando que é necessário colocar as casas decimais separadas por '.'") # Orientações ao usuário
num_1 = float(input("Insira aqui o primeiro número da operação: ")) # Usuário insere primeiro número
